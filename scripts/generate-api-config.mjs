import { existsSync, mkdirSync, readFileSync, writeFileSync } from 'node:fs'
import { dirname, resolve } from 'node:path'
import { fileURLToPath } from 'node:url'

const rootDir = resolve(dirname(fileURLToPath(import.meta.url)), '..')
const envPath = resolve(rootDir, '.env')
const outputPath = resolve(rootDir, 'entry/src/main/ets/services/ApiKeys.ets')
const values = new Map()

if (existsSync(envPath)) {
  for (const line of readFileSync(envPath, 'utf8').split(/\r?\n/)) {
    const trimmed = line.trim()
    if (trimmed.length === 0 || trimmed.startsWith('#')) {
      continue
    }
    const separator = trimmed.indexOf('=')
    if (separator > 0) {
      values.set(trimmed.slice(0, separator).trim(), trimmed.slice(separator + 1).trim())
    }
  }
}

function getValue(name, placeholder) {
  return process.env[name] ?? values.get(name) ?? placeholder
}

const googleMapsApiKey = getValue('GOOGLE_MAPS_API_KEY', 'YOUR_GOOGLE_MAPS_API_KEY')
const openWeatherApiKey = getValue('OPENWEATHER_API_KEY', 'YOUR_OPENWEATHER_API_KEY')
const content = `// Generated by scripts/generate-api-config.mjs. Do not commit this file.\n\nexport const GOOGLE_MAPS_API_KEY: string = ${JSON.stringify(googleMapsApiKey)}\nexport const OPENWEATHER_API_KEY: string = ${JSON.stringify(openWeatherApiKey)}\n`

mkdirSync(dirname(outputPath), { recursive: true })
writeFileSync(outputPath, content, 'utf8')
